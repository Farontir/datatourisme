export const generateCSSVariables = (tokens: typeof designTokens) => {
  const variables: Record<string, string> = {};
  
  const processObject = (obj: any, prefix = '') => {
    Object.entries(obj).forEach(([key, value]) => {
      const cssVar = prefix ? `${prefix}-${key}` : key;
      
      if (typeof value === 'object' && value !== null && !Array.isArray(value)) {
        processObject(value, cssVar);
      } else {
        variables[`--${cssVar}`] = String(value);
      }
    });
  };
  
  processObject(tokens);
  return variables;
};

export const cssVariables = generateCSSVariables(designTokens);